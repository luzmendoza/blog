<?php

namespace App\Policies;

use App\User;
use App\Post;
use Illuminate\Auth\Access\HandlesAuthorization;

class PostPolicy
{
    use HandlesAuthorization;

    //crear un permiso para acciones cuando es administrador, el administrador puede hacer todo
    public function before(User $user)
    {
        if ($user->hasRole('Admin')) {
            return true;
        }
    }
    
    /**
     * Determine whether the user can view any posts.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function viewAny(User $user)
    {
        //
    }

    /**
     * Determine whether the user can view the post.
     *
     * @param  \App\User  $user
     * @param  \App\Post  $post
     * @return mixed
     */
    public function view(User $user, Post $post)
    {
        //refresa true si el usuario logeado es igual que el usuario del post o si tiene permisos para verlos
        return $user->id === $post->user_id 
            || $user->hasPermissionTo('view posts');
    }

    /**
     * Determine whether the user can create posts.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function create(User $user)
    {
        //permiso a la accion
        return $user->hasPermissionTo('create posts');
    }

    /**
     * Determine whether the user can update the post.
     *
     * @param  \App\User  $user
     * @param  \App\Post  $post
     * @return mixed
     */
    public function update(User $user, Post $post)
    {
        //refresa true si el usuario logeado es igual que el usuario del post
        return $user->id === $post->user_id 
            || $user->hasPermissionTo('update posts');
    }

    /**
     * Determine whether the user can delete the post.
     *
     * @param  \App\User  $user
     * @param  \App\Post  $post
     * @return mixed
     */
    public function delete(User $user, Post $post)
    {
        //refresa true si el usuario logeado es igual que el usuario del post
        return $user->id === $post->user_id
            || $user->hasPermissionTo('delete posts');
    }

